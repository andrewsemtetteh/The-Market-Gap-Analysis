# the categories function takes the category tags of a product and maps them to a simplified classification
def categories(category_tags):
    # converts the input to a lowercase string to ensure consistent keyword matching across the dataset
    tags = str(category_tags).lower()

    # snack sub categories
    if 'chocolate' in tags or 'chocolat' in tags:
        return 'Chocolate'

    elif any(tag in tags for tag in ['chips', 'crisps', 'popcorn', 'pretzel', 'crackers', 'tortilla', 'nachos']):
        return 'Chips & Crisps'

    elif any(tag in tags for tag in ['salty', 'salted', 'craquelins', 'appetizers']):
        return 'Salty Snacks'

    elif any(tag in tags for tag in ['cookie', 'biscuit', 'wafer', 'cake', 'muffin', 'pastry', 'madeleine', 'viennoiseries', 'doughnut', 'dessert', 'pie', 'sweet-snacks']):
        return 'Cookies & Cakes'

    elif any(tag in tags for tag in ['granola', 'protein-bar', 'energy-bar', 'cereal-bar', 'protein-shake', 'proteiques', 'supplement', 'whey', 'bars']):
        return 'Bars & Granola'

    elif any(tag in tags for tag in ['nut', 'seed', 'peanut', 'almond', 'cashew', 'walnut', 'pistachio', 'trail mix', 'arachide', 'trail-mix']):
        return 'Nuts & Seeds'

    elif any(tag in tags for tag in ['confectionery', 'bonbon', 'candy', 'caramel', 'fudge', 'marshmallow', 'licorice', 'confectioneries', 'candies', 'gummi-candies']):
        return 'Candies & Sweets'

    elif any(tag in tags for tag in ['beverage', 'drink', 'juice', 'water', 'coffee', 'tea', 'soda', 'cola']):
        return 'Beverages'

    elif any(tag in tags for tag in ['dairy', 'milk', 'cheese', 'yogurt', 'yaourt', 'pudding', 'skyr', 'mousse', 'ice-cream', 'dairies', 'creams']):
        return 'Dairy'

    elif any(tag in tags for tag in ['fruit', 'vegetable', 'plant-based', 'legume', 'compote', 'apricot', 'date', 'raisins', 'prugne']):
        return 'Fruits, Vegetables & Legumes'

    elif any(tag in tags for tag in ['grain', 'bread', 'pasta', 'rice', 'cereal', 'flour']):
        return 'Grains & Baked Goods'

    elif any(tag in tags for tag in ['meat', 'poultry', 'fish', 'seafood', 'beef', 'chicken', 'pork']):
        return 'Meat & Seafood'

    elif any(tag in tags for tag in ['meal', 'prepared', 'pizza', 'soup', 'stew', 'ravioli', 'burger', 'sandwich', 'burrito', 'tamales', 'mexican-dinner-mixes']):
        return 'Prepared Meals'

    elif any(tag in tags for tag in ['oil', 'fat', 'butter', 'margarine']):
        return 'Fats & Oils'

    elif any(tag in tags for tag in ['sauce', 'spice', 'condiment', 'ketchup', 'mustard', 'pesto', 'syrup', 'sweetener', 'honey', 'spreads', 'tapenade', 'salsa']):
        return 'Sauces & Spices'

    else:
        return 'Other Snacks'

