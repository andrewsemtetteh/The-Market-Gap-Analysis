protein_keywords = ['soy', 'soja', 'whey', 'lactoserum', 'milk', 'lait', 'egg', 'oeuf', 'peanut', 'cacahuete', 'almond', 'amande', 'pea ', 'pois', 'lentil', 'lentille', 'chickpea', 'pois chiche', 'wheat gluten', 'seitan', 'casein', 'beef', 'chicken', 'poulet', 'fish', 'poisson']

found_sources = []
for text in ingredients_list:
    for source in protein_keywords:
        if source in text:
            found_sources.append(source)

# dictionary counting without Counter
counts_dict = {}
for item in found_sources:
    if item in counts_dict:
        counts_dict[item] += 1
    else:
        counts_dict[item] = 1

#convert to list of [count, name] and sort
sorting_list = []
for name, count in counts_dict.items():
    sorting_list.append([count, name])

# sort descending by count
sorting_list.sort(reverse=True)

#extractin top sources
top_sources = []
for i in range(min(3, len(sorting_list))):
    count, name = sorting_list[i]
    top_sources.append((name, count))

print("Top 3 Common Protein Sources in the High Protein Data:")
rank = 1
for item in top_sources:
    source = item[0]
    count = item[1]
    print(f"{rank}. {source.strip().upper()} (found in {count} products)")
    rank += 1

# visual representation
plt.figure(figsize=(10, 5))
